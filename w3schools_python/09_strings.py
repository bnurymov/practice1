# Python Strings

s = "Hello, World!"
print(s)
print(len(s))           # Length
print(s[0])             # First character
print(s[-1])            # Last character
print(s[0:5])           # Slicing
print(s.upper())        # Uppercase
print(s.lower())        # Lowercase
print(s.replace("H", "J"))
print("World" in s)     # Check substring
print(s.split(", "))    # Split
