# Python Numbers

x = 10      # int
y = 2.5     # float
z = 1+2j    # complex

print(x, type(x))
print(y, type(y))
print(z, type(z))

# Math operations
print(10 + 3)   # Addition
print(10 - 3)   # Subtraction
print(10 * 3)   # Multiplication
print(10 / 3)   # Division
print(10 // 3)  # Floor division
print(10 % 3)   # Modulo
print(10 ** 3)  # Power
