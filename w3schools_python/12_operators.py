# Python Operators

# Arithmetic
print(10 + 3)   # 13
print(10 - 3)   # 7
print(10 * 3)   # 30
print(10 / 3)   # 3.33...
print(10 // 3)  # 3
print(10 % 3)   # 1
print(2 ** 8)   # 256

# Comparison
print(5 == 5)   # True
print(5 != 3)   # True
print(5 > 3)    # True

# Logical
print(True and False)  # False
print(True or False)   # True
print(not True)        # False

# Identity
x = ["a", "b"]
y = x
print(x is y)          # True
print(x is not y)      # False
